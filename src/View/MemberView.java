    private void loadBookData() {
        List<Book> books = bookManagement.getBookList(); 
        tableModel = (DefaultTableModel) jTable1.getModel();
        tableModel.setRowCount(0); 

        for (Book book : books) {
            tableModel.addRow(new Object[]{
                book.getBookID(),
                book.getBookName(),
                book.getAuthorName(),
                book.getPublishedYear(),
                book.getPrice(),
                book.getGenre()
            });
        }
    }

